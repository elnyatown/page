ЕЛЬНИНСКАЯ ОПЕРАЦИЯ
Г. К. Жуков,Маршал Советского Союза

В штабе фронта задержался ненадолго. Ознакомившись с последними событиями на участках фронта, вместе с командующим артиллерией генералом Л. А. Говоровым отправился в район Ельни, в штаб 24–й армии. Приехали туда поздно вечером. По дороге видели большие зарева пожаров где–то в районе Ярцева и Ельни.

Что горело – неизвестно, но вид пожарищ производил тяжкое впечатление. Гибло в огне народное добро, многолетний труд советских людей. Я спрашивал себя: как и чем должен ответить врагу советский народ за беды, которые фашисты сеют на своем кровавом пути? Мечом, и только мечом, беспощадно уничтожая злобного врага,– был единственный ответ...

В штабе 24–й армии нас встретил командарм К. И. Ракутин и командующие родами войск. К. И. Ра–кутина я раньше не знал. Докладывая дислокацию, он произвел на меня хорошее впечатление, но его оперативно–тактическая подготовка была явно недостаточной. Ему был присущ тот же недостаток, что офицерам и генералам, работавшим ранее в пограничных войсках Наркомата внутренних дел, где им почти не приходилось совершенствоваться в вопросах оперативного искусства.

Рано утром следующего дня вместе с К. И. Ракути–ным мы выехали в район Ельни для личной рекогносцировки. В это время там шел огневой бой с противником. После обсуждения обстановки с командирами частей мы убедились, что немецкие войска здесь хорошо организовали оборону и, видимо, будут драться крепко. На переднем крае обороны и в ее глубине противник закопал в землю танки, штурмовые и артиллерийские орудия и тем самым превратил ельнинский выступ в своеобразный укрепленный район.



